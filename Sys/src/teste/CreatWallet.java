    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double c = Double.parseDouble(coin.getText());
        String pk = r_pKey.getText();
        StringBuilder sb = new StringBuilder(pk);
        boolean b = false;
        try {
            PublicKey puk = Wallet.chercher(sb);
            b = this.w.makeTransaction(puk, c);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(CreatWallet.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidKeyException ex) {
            Logger.getLogger(CreatWallet.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SignatureException ex) {
            Logger.getLogger(CreatWallet.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(CreatWallet.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        coin.setText("");
        r_pKey.setText("");
        if(b){
            valid.setText("valid!");
            valid.setForeground(new Color(31, 136, 9));
        } else {
            valid.setText("non valid!");
            valid.setForeground(new Color(255, 0, 0));
        }
        valid.setVisible(true);
        changeBalance(this.index, this.w);
        
        valider();
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
    private boolean valider(){
        if(Wallet.TransactionVerifier.size() >= 4){
            start.setEnabled(true);
            return true;
        } else {
            start.setEnabled(false);
            return false;
        }
    }
   
    private void startActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startActionPerformed
        if(!valider()){
            return;
        }
        boolean respons = false; 
        try {
            respons = this.w.mining(this);
        } catch (Exception ex) {
            Logger.getLogger(CreatWallet.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (respons){
            hashValide.setText("Block is maked !");
            System.out.println(Blockchain.blockchain.size());
        }else {
            hashValide.setText("Block is not maked !");
        }
        
        valider();
    }//GEN-LAST:event_startActionPerformed

