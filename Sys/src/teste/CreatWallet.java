    private int chercheIndex(StringBuilder sb){
        int temp = 0;
        for(CreatWallet w: CreatWallet.list){
            if (Calculhash.toHexa(w.w.getPublicKey()).equals(sb)){
                temp =  w.index;
                break;
            }
        }
        return temp;   
    }
    
